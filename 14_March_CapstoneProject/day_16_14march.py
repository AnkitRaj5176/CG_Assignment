# Import libraries
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.linear_model import LogisticRegression

# Training data
sentences = [
    "Push-ups strengthen chest and triceps",
    "Yoga improves flexibility and balance",
    "Apples are rich in fiber",
    "Cardio exercises boost heart health",
    "Bananas provide potassium",
    "Running improves stamina"
]

labels = [
    "exercise",
    "exercise",
    "nutrition",
    "exercise",
    "nutrition",
    "exercise"
]

# Convert text into numerical vectors
vectorizer = TfidfVectorizer()
X = vectorizer.fit_transform(sentences)

# Train classifier
model = LogisticRegression()
model.fit(X, labels)

# Chatbot function
def wellness_chatbot(user_input):
    vec = vectorizer.transform([user_input])
    prediction = model.predict(vec)[0]

    if prediction == "exercise":
        return "💪 This looks like an exercise topic. I can suggest workouts!"
    elif prediction == "nutrition":
        return "🥗 This is about nutrition. I can provide diet tips!"
    else:
        return "🌿 This relates to general wellness."

# Test chatbot
while True:
    user = input("Ask something about fitness: ")
    if user.lower() == "exit":
        break

    response = wellness_chatbot(user)
    print("Bot:", response)

# Scenario: Healthcare Chatbot – Patient Symptom Matching
# Imagine you’re designing a healthcare chatbot that helps patients find possible conditions based on their symptoms. Both patients and conditions are represented as vectors of features (e.g., fever, cough, fatigue, headache).
# - Patient’s symptom vector:
# [0.2,0.8,-0.3,0.5] → mild fever, strong cough, no fatigue, moderate headache.
# - Condition’s symptom profile vector:
# [0.1,0.9,-0.2,0.4] → mild fever, strong cough, little fatigue, moderate headache.
# The chatbot uses cosine similarity to measure how closely the patient’s symptoms align with the condition’s profile

import numpy as np

# Patient symptom vector
patient = np.array([0.2, 0.8, -0.3, 0.5])

# Condition symptom vector
condition = np.array([0.1, 0.9, -0.2, 0.4])

# Dot product
dot_product = np.dot(patient, condition)

# Magnitudes
norm_patient = np.linalg.norm(patient)
norm_condition = np.linalg.norm(condition)

# Cosine similarity
cosine_similarity = dot_product / (norm_patient * norm_condition)

print("Cosine Similarity:", cosine_similarity)

