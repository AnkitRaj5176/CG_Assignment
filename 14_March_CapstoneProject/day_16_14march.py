# Hospital FAQ Assistant using Embeddings

from sentence_transformers import SentenceTransformer
from sklearn.metrics.pairwise import cosine_similarity

# Load embedding model
model = SentenceTransformer('all-MiniLM-L6-v2')

# Hospital FAQ knowledge base
faqs = [
    "How can I book an appointment with a doctor?",
    "What are the hospital visiting hours?",
    "How can I pay my medical bill?",
    "Where is the emergency department located?",
    "How do I cancel or reschedule my appointment?"
]

answers = [
    "You can book an appointment through our website or call the hospital reception.",
    "Visiting hours are from 9 AM to 7 PM every day.",
    "Medical bills can be paid online, at the billing counter, or through our mobile app.",
    "The emergency department is located on the ground floor near the main entrance.",
    "You can cancel or reschedule your appointment through the patient portal or by calling reception."
]

# Convert FAQs into embeddings
faq_embeddings = model.encode(faqs)

# Function to find best matching FAQ
def find_best_answer(query):

    # Convert query to embedding
    query_embedding = model.encode([query])

    # Compute cosine similarity
    similarities = cosine_similarity(query_embedding, faq_embeddings)

    # Find best match index
    best_match = similarities.argmax()

    return answers[best_match]


